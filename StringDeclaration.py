#String Declaration:

# In Python, we can declare strings in 3 ways: 

# Way 1:

String_way1='Hi World !!!'      
print(String_way1)              #Output: Hi World !!!

# Way 2:

String_way2="Hello World !!!"    
print(String_way2)              #Output: Hello World !!!

# Way 3:

String_way3='''Wassup World !!!'''  
print(String_way3)              #Output: Wassup World !!!

